package statements;

public class SwitchCase {

	public static void main(String[] args) {
		int a=2;
		
		switch (a) {
		case 1: System.out.println("Chrome");
			
			break;
		case 2: System.out.println("Edge");
		
		break;
		
		case 3: System.out.println("Safari");
		
		break;



		default:
		System.out.println("not valid");
		}

	}

}
